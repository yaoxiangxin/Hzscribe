import UIKit

extension UIFont {
    
    class func bs_GothamBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Gotham-Bold", size: size)!
    }

    class func bs_GothamBook(size: CGFloat) -> UIFont {
        return UIFont(name: "Gotham-Book", size: size)!
    }

    class func bs_GothamBookItalic(size: CGFloat) -> UIFont {
        return UIFont(name: "Gotham-BookItalic", size: size)!
    }

    class func bs_GothamLight(size: CGFloat) -> UIFont {
        return UIFont(name: "Gotham-Light", size: size)!
    }

    class func bs_GothamMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "Gotham-Medium", size: size)!
    }
    
}
